#include "libft.h"
#include <stddef.h>
#include <unistd.h>

void			ft_putendl_fd(char *s, int fd)
{
	if (!s || fd < 0 || fd > FOPEN_MAX)
		return ;
	while (*s)
		ft_putchar_fd(*s++, fd);
	write(fd, &"\n", 1);
}

